#include <iostream>
#include <cmath>

int main() {
    std::cout << "9+2 = " << (9 + 2) << std::endl;
    std::cout << "5-2 = " << (5 - 2) << std::endl;
    std::cout << "10/2 = " << (10 / 2) << std::endl;
    std::cout << "10 x 2 = " << (10 * 2) << std::endl;
    std::cout << "6 ^ 2 = " << pow(6 , 2) << std::endl;
}
